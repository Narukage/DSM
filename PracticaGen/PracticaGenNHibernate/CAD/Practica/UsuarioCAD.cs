public string New_ (UsuarioEN usuario)
{
        try
        {
                SessionInitializeTransaction ();
                if (usuario.Admin != null) {
                        // Argumento OID y no colecci√≥n.
                        usuario.Admin = (PracticaGenNHibernate.EN.Practica.AdminEN)session.Load (typeof(PracticaGenNHibernate.EN.Practica.AdminEN), usuario.Admin.Email);

                        usuario.Admin.Usuario
                        .Add (usuario);
                }
                if (usuario.Direccion != null) {
                        for (int i = 0; i < usuario.Direccion.Count; i++) {
                                usuario.Direccion [i] = (PracticaGenNHibernate.EN.Practica.DireccionEN)session.Load (typeof(PracticaGenNHibernate.EN.Practica.DireccionEN), usuario.Direccion [i].Id);
                                usuario.Direccion [i].Usuario.Add (usuario);
                        }
                }

                session.Save (usuario);
                SessionCommit ();
        }

        catch (Exception ex) {
                SessionRollBack ();
                if (ex is PracticaGenNHibernate.Exceptions.ModelException)
                        throw ex;
                throw new PracticaGenNHibernate.Exceptions.DataLayerException ("Error in UsuarioCAD.", ex);
        }


        finally
        {
                SessionClose ();
        }

        return usuario.Email;
}

